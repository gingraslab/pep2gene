// Package helpers contains utility functions
package helpers

// SliceUnique returns the unique entries in a slice
func SliceUnique(stringSlice []string) []string {
	keys := make(map[string]bool)
	list := []string{}
	for _, entry := range stringSlice {
		if _, value := keys[entry]; !value {
			keys[entry] = true
			list = append(list, entry)
		}
	}
	return list
}
